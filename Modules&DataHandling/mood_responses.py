def mood_response(mood):
        if mood == "happy":
            return "That's great to hear!"
        if mood == "excited":
            return "What are you excited about?"
        if mood == "anxious":
            return "Try to find some peace."
        if mood == "sad":
             return "I hope your day gets better"
        else:
             return "Thats not a real emotion! How do you REALLY feel?"